// no direct access
defined('_JEXEC') or die('Restricted access');

$html = array();

if ($this->params->get('show_page_heading', 1)) : 
	$html[] = '<h1>';
	if ($this->escape($this->params->get('page_heading'))) :
		$html[] = $this->escape($this->params->get('page_heading')); 
	else : 
		$html[] = $this->escape($this->params->get('page_title')); 
	endif;
	$html[] = '</h1>';
endif; 

$message = $this->params->get('addservererrormessage');

if ($message != '')
{
	$html[] = $message;
}
else
{
	$html[] = JText::_('COM_GAMESERVER_ADDSERVER_NOACCESS');	
}

echo implode("\n", $html); 

